<template>
	<div class="InputColor">
		<template v-if="isHex">
			<InputString
				class="left"
				:value="hasAlpha ? value[1][0] : value[1]"
				:validator="validateColorHex"
				@input="hasAlpha ? onUpdateElement(0, $event) : onUpdateElement(null, $event)"
			/>
			<InputColorElement
				v-if="hasAlpha"
				class="right"
				:color="value"
				:varying="1"
				@update:element="onUpdateElement(1, $event)"
			/>
		</template>
		<template v-else>
			<InputColorElement
				class="left"
				:color="value"
				:varying="0"
				@update:element="onUpdateElement(0, $event)"
			/>
			<InputColorElement
				class="middle"
				:color="value"
				:varying="1"
				@update:element="onUpdateElement(1, $event)"
			/>
			<InputColorElement
				:class="hasAlpha ? 'middle' : 'right'"
				:color="value"
				:varying="2"
				@update:element="onUpdateElement(2, $event)"
			/>
			<InputColorElement
				v-if="hasAlpha"
				class="right"
				:color="value"
				:varying="3"
				@update:element="onUpdateElement(3, $event)"
			/>
		</template>
	</div>
</template>

<script lang="ts">
import {Component, Prop, Vue} from 'vue-property-decorator'

import InputColorElement from './InputColorElement.vue'
import InputString from '../InputString.vue'

import {DataColor, DataColorMode, DataColorInfo} from '../../data'

@Component({
	components: {InputColorElement, InputString}
})
export default class InputColor extends Vue {
	@Prop(Array) private value!: DataColor
	@Prop({type: Boolean, default: true}) private showLabel!: boolean

	private get hasAlpha(): boolean {
		return this.value[0].indexOf('a') !== -1
	}

	private get isHex(): boolean {
		return this.value[0].indexOf('hex') !== -1
	}

	private validateColorHex(value: string): string | false {
		if (/^#?(?:[0-9A-F]{3}){1,2}$/i.test(value)) {
			if (!value.startsWith('#')) {
				console.warn('dont start with #')
				value = '#' + value
			}
			return value.toUpperCase()
		} else {
			return false
		}
	}

	private onUpdateElement(index: number, element: number | string) {
		const elements = Array.from(this.value[1])
		elements[index] = element
		const newValue = [this.value[0], elements]
		this.$emit('input', newValue)
	}
}
</script>

<style lang="stylus" scoped>
.InputColor
	display flex
	user-select none
</style>
